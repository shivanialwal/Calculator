public class Calculator {
    public String add(int a, int b){
        return "Adding b and b will give you "+(a + b);
    }
    public String sub(int a, int b){
        return "Subtracting b and b will give you "+(a - b);
    }
    public String multiply(int a, int b){
        return "Multiplying b and b will give you "+(a * b);
    }
    public String divide(int a, int b){
        return "Dividing b and b will give you "+(a / b);
    }
}
